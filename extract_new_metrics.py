
import pandas as pd
import json
import os

path = r"d:\RetailСrm.ru\Итоговые данные\Гранд_Сводка_PiPiWood_Цветная.xlsx"

def extract_metrics():
    df = pd.read_excel(path)
    
    # 1. Основные метрики
    # 'Всего_покупок_шт' - это сумма покупок клиента за все время. 
    # Но в сводке 'Гранд_Сводка' обычно одна строка = один клиент.
    # Если это сводка по клиентам, то количество заказов - это сумма колонки 'Всего_покупок_шт'.
    # Сумма покупок (LTV) - нам нужно понять, есть ли такая колонка. 
    # В предыдущем чек-листе ее не было видно в первых колонках.
    
    print("Columns:", df.columns.tolist())
    
    # Пытаемся найти или рассчитать LTV (Выручку)
    # Если колонки с выручкой нет, возможно она в Комментарии или скрыта.
    # Судя по PiPiWood_Grand_Dashboard.html, там есть суммы.
    
    # Новая попытка анализа:
    # Обычно в таких сводках есть колонка 'Сумма' или 'LTV'.
    
    # Если мы не видим выручку напрямую, возможно она рассчитывается.
    # Давайте глянем больше строк.
    
    metrics = {
        "total_clients": len(df),
        "total_orders": int(df['Всего_покупок_шт'].sum()) if 'Всего_покупок_шт' in df.columns else 0
    }
    
    # Ищем ТОП-15
    # Сортируем по покупкам, так как выручку пока не нашли
    top_15 = df.sort_values('Всего_покупок_шт', ascending=False).head(15)
    
    print("\nMetrics:", metrics)
    print("\nTop 5 Clients by count:")
    print(top_15[['Имя', 'Телефон', 'Город', 'Всего_покупок_шт']].head(5))

extract_metrics()
